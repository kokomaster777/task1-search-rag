"""Скрипт для демонстрации разницы поисков: гоняет подготовленные запросы
через /search/compare и печатает таблицу. Запускать при поднятом docker compose."""
import requests

BASE = "http://localhost:8000"

# Запросы подобраны так, чтобы показать сильные/слабые стороны каждого подхода
QUERIES = [
    # Семантика должна выигрывать: синонимы и перефразировки
    "лэптоп для командировок",          # в названиях — «ноутбук»
    "устройство следить за сном",       # фитнес-браслет
    "что-то тёплое на зиму",            # куртки/пуховики
    "подарок ребенку 5 лет",            # детские товары
    "техника которая сама убирает квартиру",  # робот-пылесос
    # Полнотекст должен выигрывать: точные термины и бренды
    "ноутбук Lenovo 16/512",
    "кофе Jardin 250 г",
    "Redmond мультиварка",
    # Опечатки: pg_trgm vs эмбеддинги
    "нотбук асус",
    "кросовки для бега",
]

for q in QUERIES:
    r = requests.get(f"{BASE}/search/compare", params={"q": q, "limit": 3}).json()
    print(f"\n=== «{q}» ===")
    ft = r["fulltext"] or [{"name": "— ничего не найдено —"}]
    vec = r["vector"]
    print("  FTS (Postgres):     " + " | ".join(x["name"][:45] for x in ft[:3]))
    print("  Vector (Qdrant):    " + " | ".join(x["name"][:45] for x in vec[:3]))
