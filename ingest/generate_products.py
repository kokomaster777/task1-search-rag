def generate():
    products = []
    pid = itertools.count(1)
    for category, (types, brands, attrs, descs) in CATALOG.items():
        for t in types:
            for _ in range(random.randint(8, 12)):
                brand = random.choice(brands)
                attr = ", ".join(random.sample(attrs, k=random.randint(1, 2)))
                name = f"{t} {brand} {attr}"
                desc = random.choice(descs).replace("{n}", str(random.randint(2, 12)))
                products.append({
                    "id": next(pid),
                    "name": name,
                    "category": category,
                    "brand": brand,
                    "price": round(random.uniform(199, 120000), 0),
                    "description": desc,
                })
    return products


if __name__ == "__main__":
    products = generate()
    os.makedirs("data", exist_ok=True)
    with open("data/products.json", "w", encoding="utf-8") as f:
        json.dump(products, f, ensure_ascii=False, indent=1)
    print(f"Сгенерировано {len(products)} товаров в {len(CATALOG)} категориях -> data/products.json")
